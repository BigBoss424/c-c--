#include <iostream>

using namespace std;

int main(){

    //Integer is a data type that holds a whole number
    int a = 81;

    //Float is the original data type which includes a decimal. 
    float b = 1.23456;

    //Double is a variable is a upgrade from float.
    double c = 3.14;

    //char is a variable that holds a character
    char d = 'a';
    
    //Declare two boolean variables
    //Boolean variables are declared as either true or false;
    bool e = true;
    bool f = false;

    //Each data type can be used in the following 
    cout << "The integer a: " << a << endl;
    cout << "The float b: " << b << endl;
    cout << "The double c: " << c << endl;
    cout << "The char d: " << d << endl;
    cout << "The boolean e: " << e << endl;
    cout << "The boolean f: " << f << endl;


    return 0;
}
